class DaggerControl(object):
    """Class that handles Dagger Training."""
    def __init__(self, world, model_path):
        self.world = world
        if isinstance(world.player, carla.Vehicle):
            self._control = carla.VehicleControl()
            self._lights = carla.VehicleLightState.NONE
            world.player.set_light_state(self._lights)
        else:
            raise NotImplementedError("Actor type not supported")
        self._steer_cache = 0.0

        self.gpu = torch.device('cuda')

        self.model = models.RegressionNetwork().to(self.gpu)
        self.model.load_state_dict(torch.load(model_path, weights_only=True))
        self.camera = world.camera_manager
        self.transform = transforms.Compose([
                    transforms.ToTensor(),
                    ])

    def train_dagger(self, clock, display):
        train_obs = []
        train_actions = []
        for i in range(25):
            print("Starting new dagger iter: ", i)
            images, actions = self.get_expert_actions(self.world, clock, display, train_obs, train_actions)
            train_obs = images
            train_actions = actions
            # tensorImages = [self.transform(image) for image in images]
